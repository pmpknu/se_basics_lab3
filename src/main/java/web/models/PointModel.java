package web.models;

public class PointModel {
    private int id;
    private double x;
    private double y;
    private double r;

    private boolean inArea;

    public int getId() {
        return id;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getR() {
        return r;
    }

    public boolean isInArea() {
        return inArea;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    public void setR(double r) {
        this.r = r;
    }

    public void setInArea(boolean inArea) {
        this.inArea = inArea;
    }
}
